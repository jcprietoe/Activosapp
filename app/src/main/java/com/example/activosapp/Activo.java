package com.example.activosapp;

public class Activo {
    private String nombreActivo;
    private String areaActivo;
    private String estadoActivo;
    private String variableControlActivo;
    private String URLAvatarActivo;


    public String getEstadoActivo() {
        return estadoActivo;
    }

    public void setEstadoActivo(String estadoActivo) {
        this.estadoActivo = estadoActivo;
    }

    public String getVariableControlActivo() {
        return variableControlActivo;
    }

    public void setVariableControlActivo(String variableControlActivo) {
        this.variableControlActivo = variableControlActivo;
    }

    public String getURLAvatarActivo() {
        return URLAvatarActivo;
    }

    public void setURLAvatarActivo(String URLAvatarActivo) {
        this.URLAvatarActivo = URLAvatarActivo;
    }

    public String getAreaActivo() {
        return areaActivo;
    }

    public void setAreaActivo(String areaActivo) {
        this.areaActivo = areaActivo;
    }

    public String getNombreActivo() {
        return nombreActivo;
    }

    public void setNombreActivo(String nombreActivo) {
        this.nombreActivo = nombreActivo;
    }
}
